const createMinorArcanaCard = (
  suit: 'wands' | 'cups' | 'swords' | 'pentacles',
  number: number,
  name: string,
  keywords: string[],
  uprightMeaning: string,
  reversedMeaning: string,
  description: string,
  element: string
): TarotCard => ({
  id: `${number}_of_${suit}`,
  name: `${name} of ${suit.charAt(0).toUpperCase() + suit.slice(1)}`,
  arcana: 'minor',
  number,
  suit,
  keywords,
  uprightMeaning,
  reversedMeaning,
  description,
  element
});

